namespace Calculator.Business
{
    public class SimpleCalculator
    {
        /// <summary>
        /// finds the sum of two ints and returns the sum
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int FindSum(int a, int b)
        {
            return a + b;
        }
    }
}
